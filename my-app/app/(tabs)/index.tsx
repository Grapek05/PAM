import {Alert, Modal, Pressable } from 'react-native';
import {SafeAreaView, SafeAreaProvider} from 'react-native-safe-area-context';



import React, { useState } from 'react';
import { StyleSheet, Text, View, Button } from 'react-native';
import { StatusBar } from 'expo-status-bar';

const Mop = () => {
  const [modalVisible, setModalVisible] = useState(false);
  return (
    <SafeAreaProvider>
      <SafeAreaView style={styles.centeredView}>
        <Modal
          animationType="slide"
          transparent={true}
          visible={modalVisible}
          onRequestClose={() => {
            Alert.alert('Modal has been closed.');
            setModalVisible(!modalVisible);
          }}>
          <View style={styles.centeredView}>
            <View style={styles.modalView}>
              <Text style={styles.modalText}>Hello World!</Text>
              <Pressable
                style={[styles.button, styles.buttonClose]}
                onPress={() => setModalVisible(!modalVisible)}>
                <Text style={styles.textStyle}>Hide Modal</Text>
              </Pressable>
            </View>
          </View>
        </Modal>
        <Pressable
          style={[styles.button, styles.buttonOpen]}
          onPress={() => setModalVisible(true)}>
          <Text style={styles.textStyle}>Show Modal</Text>
        </Pressable>
      </SafeAreaView>
    </SafeAreaProvider>
  );
};

function Header() {
  return (
    <View>
      <Text style={{ fontSize: 24 }}>Aplikacja Studencka</Text>
    </View>
  );
}

export default function App() {
  // State must be inside the component function
  const [count, setCount] = useState<number>(0);

  return (
    <View style={styles.container}>
      {/* Header Section */}
      <Text style={styles.title}>Smart Campus</Text>
      <Text style={styles.subtitle}>Moja pierwsza aplikacja mobilna</Text>
      
      <View style={styles.divider} />

      {/* Counter Section */}
      <Text style={styles.counterTitle}>Licznik</Text>
      <Text style={styles.counterValue}>{count}</Text>
      
      <View style={styles.buttonContainer}>
        <Button 
          title="Zwiększ" 
          onPress={() => setCount(count + 1)} 
          color="#007AFF"
        />
      </View>
      <Mop />
      <Header />
      <StatusBar style="auto" />
    </View>
  );
}

